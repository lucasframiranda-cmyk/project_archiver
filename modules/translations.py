@dataclass
class TranslationManager:
    language: str = "PT"

    def set_language(self, language: str) -> None:
        self.language = language if language in TRANSLATIONS else "PT"

    def get(self, key: str, **kwargs) -> str:
        text = TRANSLATIONS.get(self.language, TRANSLATIONS["PT"]).get(key, key)
        return text.format(**kwargs) if kwargs else text

    def month_name(self, month: int) -> str:
        return self.get(f"month_{month}")
